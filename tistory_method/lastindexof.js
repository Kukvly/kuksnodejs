/**
 * lastIndexOf() 메서드는 배열에서 주어진 값을 발견할 수 있는
 * 마지막 인덱스를 반환하고, 요소가 존재하지 않으면 -1을 반환합니다.
 * 배열 탐색은 fromIndex에서 시작하여 뒤로 진행합니다.
*/

const kakao = ['m', 'i', 'l', 'k'];

console.log(kakao.lastIndexOf('m'));

console.log(kakao.lastIndexOf('i'));

console.log(kakao.lastIndexOf('l'));

console.log(kakao.lastIndexOf('k'));

console.log(kakao.lastIndexOf('e'));